def _render_structure(structure=None, indent=0):
    """将结构渲染为缩进文本，叶子节点格式为「- key: 中文名：注释」"""
    if structure is None:
        structure = WORLDVIEW_STRUCTURE

    lines = []
    prefix = '  ' * indent

    keys = [k for k in structure if not k.startswith('_')]
    for key in keys:
        value = structure[key]
        if isinstance(value, tuple):
            name, desc, ftype = value
            type_label = '[]' if ftype == 'list' else ''
            lines.append(prefix + f'- {key}: {name}{type_label}  // {desc}')
        elif isinstance(value, dict):
            comment = value.get('_comment', '')
            name = comment or key
            lines.append(prefix + f'{key}: {name}（字典）')
            lines.append(_render_structure(value, indent + 1))

    return '\n'.join(lines)


def _render_layer_structure(layer_key):
    """渲染单个层的结构文本"""
    layer = WORLDVIEW_STRUCTURE.get(layer_key)
    if layer is None:
        return ''
    comment = layer.get('_comment', '')
    header = f'层级：{comment or layer_key}（输出 JSON 时请直接输出以下字段，不要在外层包裹 "{layer_key}" 键）'
    return header + '\n' + _render_structure(layer)


def get_layer_info(layer_key):
    """获取层的中文名和结构文本，供优化接口使用"""
    layer = WORLDVIEW_STRUCTURE.get(layer_key, {})
    name = layer.get('_comment', layer_key)
    structure = _render_layer_structure(layer_key)
    return name, structure


def get_layers():
    """返回所有分层的 (字段名, 中文名) 列表，顺序由 WORLDVIEW_STRUCTURE 定义"""
    return [(k, v['_comment']) for k, v in WORLDVIEW_STRUCTURE.items()]


