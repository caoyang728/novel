def get_field_cn_name(layer_key, nested_path):
    """从 WORLDVIEW_STRUCTURE 获取字段中文名

    通过 nested_path（如 'geography.continent_distribution'）在结构树中查找，
    返回叶子字段的中文名称（如 '大陆分布'）。
    若路径无法解析，回退返回 nested_path 本身。

    用于构建 LLM prompt 中的 polish_instruction，告诉 LLM 哪些字段需要润色。
    """
    layer = WORLDVIEW_STRUCTURE.get(layer_key, {})
    keys = nested_path.split('.')
    current = layer
    for key in keys:
        if isinstance(current, dict):
            current = current.get(key, None)
            if current is None:
                return nested_path  # 路径断掉，回退
        else:
            return nested_path
    if isinstance(current, tuple):
        return current[0]  # (中文名, 注释, 类型) → 取中文名
    return nested_path


